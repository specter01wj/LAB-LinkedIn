""" What Makes Up an Object? """

# examine a string object
print('shirt')
print(type('shirt'))
print(dir('shirt'))

# use upper method on a string object
print('shirt'.upper())

# examine IDs of different string objects
print(id('shirt'))
print(id('pants'))

# examine an integer object
print(id(1))
print(dir(1))

# examine ID and attributes of functions
print(id(id))
print(dir(dir))
